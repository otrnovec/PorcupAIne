import argparse

def beautify(text):
    mapping = {'A': 'ğ”„', 'B': 'ğ”…', 'C': 'â„­', 'D': 'ğ”‡', 'E': 'ğ”ˆ', 'F': 'ğ”‰', 'G': 'ğ”Š', 'H': 'â„Œ', 'I': 'â„‘', 'J': 'ğ”', 'K': 'ğ”', 'L': 'ğ”', 'M': 'ğ”', 'N': 'ğ”‘', 'O': 'ğ”’', 'P': 'ğ”“', 'Q': 'ğ””', 'R': 'â„œ', 'S': 'ğ”–', 'T': 'ğ”—', 'U': 'ğ”˜', 'V': 'ğ”™', 'W': 'ğ”š', 'X': 'ğ”›', 'Y': 'ğ”œ', 'Z': 'â„¨', 'a': 'ğ”', 'b': 'ğ”Ÿ', 'c': 'ğ” ', 'd': 'ğ”¡', 'e': 'ğ”¢', 'f': 'ğ”£', 'g': 'ğ”¤', 'h': 'ğ”¥', 'i': 'ğ”¦', 'j': 'ğ”§', 'k': 'ğ”¨', 'l': 'ğ”©', 'm': 'ğ”ª', 'n': 'ğ”«', 'o': 'ğ”¬', 'p': 'ğ”­', 'q': 'ğ”®', 'r': 'ğ”¯', 's': 'ğ”°', 't': 'ğ”±', 'u': 'ğ”²', 'v': 'ğ”³', 'w': 'ğ”´', 'x': 'ğ”µ', 'y': 'ğ”¶', 'z': 'ğ”·'}
    return ''.join(mapping.get(char, char) for char in text)


def convert(text): return beautify(text)


def main(text): return convert(text)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Load text from command line argument and beautify it.")
    parser.add_argument('--text', help='Ugly text.')
    args = parser.parse_args()
    result = main(args.text)
    print(f"Received text: {result}")
